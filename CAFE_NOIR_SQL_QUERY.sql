--21. Create a stored procedure to fetch the result according to the product type from Product Table. 

CREATE PROCEDURE Get_Product_type
	@product_type NVARCHAR(50)
AS
BEGIN
	SELECT * FROM Product
	WHERE Product_Type=@product_type;
END;

