CREATE FUNCTION dbo.GetbyProductType
	(
		@Producttype NVARCHAR(50)
	)
	RETURNS TABLE
	AS 
	RETURN 
	(
		SELECT ProductId,Product,Product_Type,Type
		FROM Product
		WHERE Product_Type=@Producttype
	)

